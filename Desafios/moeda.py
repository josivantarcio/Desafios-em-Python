def aumentar(n, p=0):
    print( f'O valor de {n} com acrescimo de {p}% é: {n + (n * p / 100)}')


def diminuir(n, p=0):
    print(f'O valor de {n} com desconto de {p}% é: {n - (n * p / 100)}')


def dobro(n):
    print(f'O dobro de {n} é: {n * 2}')


def metade(n):
    print(f'O metade de {n} é: {n / 2}')